-- Realtime Chatroom table setup with Jarvis scoring
-- Run this in Supabase SQL Editor.

create table if not exists public.messages (
  id bigint generated by default as identity primary key,
  room text not null default 'main-room',
  user_name text not null check (char_length(user_name) <= 40),
  content text not null check (char_length(content) <= 4000),
  is_ai boolean not null default false,
  reply_to_message_id bigint,
  game_slot bigint,
  event_key text,
  event_type text,
  created_at timestamptz not null default now()
);

-- Safe upgrade for existing projects that already created the messages table.
alter table public.messages add column if not exists reply_to_message_id bigint;
alter table public.messages add column if not exists game_slot bigint;
alter table public.messages add column if not exists event_key text;
alter table public.messages add column if not exists event_type text;

create index if not exists messages_room_created_at_idx
on public.messages (room, created_at);

-- Prevent duplicate Jarvis replies for the same user message or game question.
create unique index if not exists messages_jarvis_one_reply_per_message_idx
on public.messages (reply_to_message_id)
where is_ai = true and reply_to_message_id is not null;

-- Prevent duplicate Jarvis game questions inside the same 2-minute slot.
create unique index if not exists messages_jarvis_one_game_per_slot_idx
on public.messages (game_slot)
where is_ai = true and user_name = 'Jarvis' and game_slot is not null;

-- Prevent duplicate system/Jarvis events when many browsers are open.
create unique index if not exists messages_unique_event_key_idx
on public.messages (event_key)
where event_key is not null;

-- Persistent all-time scoreboard.
create table if not exists public.user_scores (
  user_name text primary key check (char_length(user_name) <= 40),
  total_score integer not null default 0 check (total_score >= 0),
  updated_at timestamptz not null default now()
);

-- One score event per Jarvis game slot so only the first correct answer gets a point.
create table if not exists public.score_events (
  event_key text primary key,
  user_name text not null check (char_length(user_name) <= 40),
  points integer not null default 1 check (points > 0),
  created_at timestamptz not null default now()
);

create index if not exists user_scores_total_score_idx
on public.user_scores (total_score desc, user_name asc);

create or replace function public.award_score_once(
  p_event_key text,
  p_user_name text,
  p_points integer default 1
)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  inserted_count integer;
  clean_event_key text;
  clean_user_name text;
begin
  clean_event_key := left(trim(coalesce(p_event_key, '')), 200);
  clean_user_name := left(trim(coalesce(p_user_name, '')), 40);

  if clean_event_key = '' or clean_user_name = '' then
    return false;
  end if;

  if p_points is null or p_points <= 0 then
    return false;
  end if;

  insert into public.score_events (event_key, user_name, points)
  values (clean_event_key, clean_user_name, p_points)
  on conflict (event_key) do nothing;

  get diagnostics inserted_count = row_count;

  if inserted_count = 0 then
    return false;
  end if;

  insert into public.user_scores (user_name, total_score, updated_at)
  values (clean_user_name, p_points, now())
  on conflict (user_name) do update
  set total_score = public.user_scores.total_score + excluded.total_score,
      updated_at = now();

  return true;
end;
$$;

grant execute on function public.award_score_once(text, text, integer) to anon, authenticated;

alter table public.messages enable row level security;
alter table public.user_scores enable row level security;
alter table public.score_events enable row level security;

-- Demo policies: anyone with anon key can read and insert chat messages.
-- For production, replace these with auth-based policies.
drop policy if exists "Anyone can read chat messages" on public.messages;
create policy "Anyone can read chat messages"
on public.messages
for select
to anon, authenticated
using (true);

drop policy if exists "Anyone can insert chat messages" on public.messages;
create policy "Anyone can insert chat messages"
on public.messages
for insert
to anon, authenticated
with check (true);

-- Public read-only scoreboard for demo.
drop policy if exists "Anyone can read user scores" on public.user_scores;
create policy "Anyone can read user scores"
on public.user_scores
for select
to anon, authenticated
using (true);

-- Optional audit read for score events. Writes are handled by award_score_once().
drop policy if exists "Anyone can read score events" on public.score_events;
create policy "Anyone can read score events"
on public.score_events
for select
to anon, authenticated
using (true);

-- Enable Supabase Realtime for messages and scores without duplicate-publication errors.
do $$
begin
  if not exists (
    select 1
    from pg_publication_tables
    where pubname = 'supabase_realtime'
      and schemaname = 'public'
      and tablename = 'messages'
  ) then
    alter publication supabase_realtime add table public.messages;
  end if;

  if not exists (
    select 1
    from pg_publication_tables
    where pubname = 'supabase_realtime'
      and schemaname = 'public'
      and tablename = 'user_scores'
  ) then
    alter publication supabase_realtime add table public.user_scores;
  end if;
end $$;
